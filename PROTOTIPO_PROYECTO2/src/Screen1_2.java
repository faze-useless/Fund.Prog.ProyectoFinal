    private void btn_problem1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_problem1MouseClicked
        // TODO add your handling code here:
        String get_distance, get_speed, get_time;
        get_distance = txf_distance.getText();
        get_speed = txf_speed.getText();
        get_time = txf_time.getText();
        
        //System.out.println(get_distance+" a ");
        
        if (!get_distance.equals("") && !get_time.equals("") && get_speed.equals("")) { // formula para velocidad
            System.out.println("caso 1");
            Operate(1, get_distance, get_speed, get_time);
        } else if (!get_distance.equals("") && !get_speed.equals("") && get_time.equals("")) { // formula para tiempo
            System.out.println("caso 2");
            Operate(2, get_distance, get_speed, get_time);
        } else if (!get_speed.equals("") && !get_time.equals("") && get_distance.equals("")) { // formula para distancia
            System.out.println("caso 3");
            Operate(3, get_distance, get_speed, get_time);
        } else if (!get_distance.equals("") && get_speed.equals("") && get_time.equals("") ||
                get_distance.equals("") && !get_speed.equals("") && get_time.equals("") ||
                get_distance.equals("") && get_speed.equals("") && !get_time.equals("")) {
            System.out.println("caso 4 error 2");
            Operate(4, get_distance, get_speed, get_time);
        } else if (!get_distance.equals("") && !get_speed.equals("") && !get_time.equals("")) {
            System.out.println("caso 5 error 3");
            Operate(5, get_distance, get_speed, get_time);
        } else {
            System.out.println("caso no reconocido");
            Operate(0, get_distance, get_speed, get_time);
        }
        
    }//GEN-LAST:event_btn_problem1MouseClicked

