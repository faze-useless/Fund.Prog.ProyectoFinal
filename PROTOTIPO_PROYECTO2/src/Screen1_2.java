    public void Operate(int caso, String get_d, String get_v, String get_t) {
        Double d = null, v = null, t = null;
        
        switch(caso) {
            case 1:
                if (ValidNumber(get_d)) { // evaluar distancia
                    d = Double.valueOf(get_d);
                    txf_distance.setBackground(Color.white);
                } else {
                    JOptionPane.showMessageDialog(this,error_message1);
                    txf_distance.setBackground(Color.red);
                }
                if (ValidNumber(get_t)) { // evaluar tiempo
                    t = Double.valueOf(get_t);
                    txf_time.setBackground(Color.white);
                } else {
                    JOptionPane.showMessageDialog(this,error_message1);
                    txf_time.setBackground(Color.red);
                }
                
                if (ValidNumber(get_d) && ValidNumber(get_t)) {
                    v = d/t;
                    //String str_v = v+"";
                    txf_speed.setText(String.format("%.2f", v));
                }
                break;
            case 2:
                if (ValidNumber(get_d)) { // evaluar distancia
                    d = Double.valueOf(get_d);
                    txf_distance.setBackground(Color.white);
                } else {
                    JOptionPane.showMessageDialog(this,error_message1);
                    txf_distance.setBackground(Color.red);
                }
                if (ValidNumber(get_v)) { // evaluar tiempo
                    v = Double.valueOf(get_v);
                    txf_speed.setBackground(Color.white);
                } else {
                    JOptionPane.showMessageDialog(this,error_message1);
                    txf_speed.setBackground(Color.red);
                }
                
                if (ValidNumber(get_d) && ValidNumber(get_v)) {
                    t = d/v;
                    //String str_v = v+"";
                    txf_time.setText(String.format("%.2f", t));
                }
                break;
            case 3:
                if (ValidNumber(get_v)) { // evaluar distancia
                    v = Double.valueOf(get_v);
                    txf_speed.setBackground(Color.white);
                } else {
                    JOptionPane.showMessageDialog(this,error_message1);
                    txf_speed.setBackground(Color.red);
                }
                if (ValidNumber(get_t)) { // evaluar tiempo
                    t = Double.valueOf(get_t);
                    txf_time.setBackground(Color.white);
                } else {
                    JOptionPane.showMessageDialog(this,error_message1);
                    txf_time.setBackground(Color.red);
                }
                
                if (ValidNumber(get_v) && ValidNumber(get_t)) {
                    d = v*t;
                    //String str_v = v+"";
                    txf_distance.setText(String.format("%.2f", d));
                }
                break;
            case 4:
                JOptionPane.showMessageDialog(this,error_message2);
                break;
            case 5:
                JOptionPane.showMessageDialog(this,error_message3);
                break;
            case 0:
                break;
        }
    }
